package cs360ProjectImplementation;

public class InsertableCode {
	private String name;
	private String code;
	
	public InsertableCode(String name, String code) {
		this.name = name;
		this.code = code;
	}
	
	public String getName() {
		return name;
	}
	
	public String getCode() {
		return code;
	}
}